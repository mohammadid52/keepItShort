export const backgroundColorMode = (darkMode) => {
  const color = darkMode ? '#394867' : '#f6f6f6';
  return color;
};

export const textColorMode = (darkMode) => {
  const color = darkMode ? '#f6f6f6' : '#000';
  return color;
};

export const iconColorMode = (darkMode) => {
  const color = darkMode ? '#f6f6f6' : '#262626';
  return color;
};
